var fruit = 'orange';
if (fruit.length > 5) {
  console.log('The fruit name has more than five characters.');
}else {
  console.log('The fruit name has five characters or less.');
}
// 定義名稱寫入資料
// 檢視字串長度
// 大於等於五：The fruit name has more than five characters.
// 小於五：The fruit name has five characters or less.
